#Write a program to take a single digit number from the key board and print its value in English word using if elif
singleDigit=int(input("Enter a Digit : "))
if singleDigit==0:
    print("Zero")
elif singleDigit==1:
    print("One")
elif singleDigit==2:
    print("Two")
elif singleDigit==3:
    print("Three")
elif singleDigit==4:
    print("Four")
elif singleDigit==5:
    print("Five")
elif singleDigit==6:
    print("Six")
elif singleDigit==7:
    print("Seven")
elif singleDigit==8:
    print("Eight")
elif singleDigit==9:
    print("Nine")
else:
    print("Number is not a single digit")
